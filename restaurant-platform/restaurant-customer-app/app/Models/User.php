<?php

declare(strict_types=1);

namespace App\Models;

use Database\Factories\UserFactory;
use Illuminate\Database\Eloquent\Attributes\Fillable;
use Illuminate\Database\Eloquent\Attributes\Hidden;
use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Foundation\Auth\User as Authenticatable;
use Illuminate\Notifications\Notifiable;
use Illuminate\Support\Carbon;

/**
 * This app's own local user model — kept only because Laravel's framework
 * internals expect a default auth provider/model to exist
 * (`config/auth.php`). It is **not** how customers are authenticated: real
 * customer identity lives entirely on restaurant-backend, reached via
 * Sanctum bearer tokens (see App\Stores\AuthTokenStore,
 * App\Services\Api\ApiClient, docs/CUSTOMER_APP_AUTH.md). No local `users`
 * row is ever created for a real customer, and nothing in this app's own
 * auth screens uses Laravel's `Auth` facade/session guard.
 *
 * @property int $id
 * @property string $name
 * @property string $email
 * @property Carbon|null $email_verified_at
 * @property string $password
 * @property string|null $remember_token
 * @property Carbon|null $created_at
 * @property Carbon|null $updated_at
 */
#[Fillable(['name', 'email', 'password'])]
#[Hidden(['password', 'remember_token'])]
class User extends Authenticatable
{
    /** @use HasFactory<UserFactory> */
    use HasFactory, Notifiable;

    /**
     * Get the attributes that should be cast.
     *
     * @return array<string, string>
     */
    protected function casts(): array
    {
        return [
            'email_verified_at' => 'datetime',
            'password' => 'hashed',
        ];
    }
}
